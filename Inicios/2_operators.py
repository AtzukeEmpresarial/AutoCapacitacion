#operadores como en todos los lenguajes
print(3 + 1) #suma
print(3 - 2) #resta
print(4 / 2) #división , da siempre un flotante
print(4 % 2) #modulo
print(5 * 4) #multiplicación
print(10 // 3) #división que aproxima al entero mas proximo, flordivision
print(10 ** 3) #potenciación

print("hola" + "mundo") #concatenación
print("hola" + str(5)) #concatenación, el str convierte a str
print("hola" * 4) #multiplicador de objetos, solo admite enteros (int)

#OPERADORES COMPARATIVOS
print(3 < 4)
print(3 > 4)
print(3 <= 4)
print(3 >= 4)
print(3 == 4)
print(3 != 4)

#comparadores de ordenación alfabetica, b>a como z>s pero s>b
print("Hola" < "python")
print("Hola" > "python")
print("Hola" <= "python")
print("Hola" >= "python")
print("Hola" == "python")
print("Hola" != "python")

